        <p>Number of datasets: {{ num_datasets }}</p>
        <p>Users: {{ users }}</p>
        <p>User stats: {{ user_stats }}</p>
        <p>Base URIs: {{ base_uris }}</p>
        <p>Base URI stats: {{ base_uri_stats }}</p>
